package basic;
public class OperatorsEx {

	public static void main(String[] args) {
		//Arithmetic operator
		int x=20;
		int y=50;
		System.out.println("Addition of two numbers :"+(x+y));
		System.out.println("Substaction of two numbers :"+(x-y));
		System.out.println("Multiplication of two numbers :"+(x*y));
		System.out.println("Division  of two numbers :"+(x/y));
		System.out.println("Modulo of two numbers :"+(x%y));
	//Unary operators
		System.out.println("increment of x :"+(++x));
		System.out.println("Decrement of x :"+(--x));
		System.out.println("Invertion  of x :"+(~x));
		//logical operators
		System.out.println("Logical & operator :" +(x&y));
		System.out.println("Logical | operator :" +(x|y));
		//System.out.println("Logical ! operator :" +(!y));

	}

}
 